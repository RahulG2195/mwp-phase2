                <p>Adaran / Agiari means the house of fire, a sacred worship place of the Parsi /  Zorastrians Community. Vesu Adaran near Surat was established in the year 1889 (nearly 130 years old) by Late Sri Pirojbhai Manekji Patel that serves as holy place till date with a distinct significance of heritage to the Parsi community. Traditionally, Agiaris are meant to be nondescript and less highlighted 'being a religious place with no or little scope of extravagance in the structure as well as in the architecture. The Vesu Adaran which was constructed in the late 18th century had battled its fair share of wear and tear in the form of time, climate, pollution and natural disaster and resulted in a structure that looked pale shadow of its heyday. 
Vesu Adaran in 2018 stood as a disintegrated heritage structure calling for collaborative intelligence from varied fields for its restoration as it demanded heavy duty embellishments and complete architectural rejig. The mandate was noble and task mammoth, hence few noble men, philanthropic at heart and technical experts joined hands to take up the mantle of restoration of this heritage of Parsi community. 
Dots started to connect in 2018, wherein, it was discovered that the Adaran needed a major overhaul as the structure was decoying by the day. The Mistry Farrilly and promoters of Shapoorji Pallonji Group were approached to take the stock of the situation and lead the restoration process. The mistry family who has traced their roots to Vesu, where the late Shapoorji Pallonji Mistry was born in the year 1887, magnanimously agreed to bear the entire cost of restoration & Mr. Cyrus Mistry joined forces with Savant Heritage Conservation Pvt. Ltd., to accomplish the task. Mr. Firoze Bhathena, Executive Director of the group approached the trustees of the Adaran Trust and journey of bringing Adaran back to its heydays embarked. Savani Hertiage Conservation Pvt Ltd, a reputed name in the field of heritage conservation to accomplish the task.
