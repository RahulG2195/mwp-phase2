                <p>Indian Architecture is as old as History itself and the cornerstone to every architectural marvel has always been temples and religious structures. One such piece of architectural phenomena is Chamarajeshwara temple located in Chamarajanagar in Karnataka which is truly the most splendid depiction of Dravidian architecture. Chamarajanagara shares its border with Tamilnadu and Kerala. The Temple was named ‘Chamarajeshwara’ which means containing the attributes of the King and God. 
Carved out of a deep sense of religious affiliation and desire to please god, ‘Chamarajeshwara Temple’ is an important identification of Indian culture and its people. The temple is supposed to be built in the 10th Century, has a Hoysala design architecture showcased in the 70 feet high five tier Rajagopuram (tower) with five scintillating brass Kalasha facing the east. The garbagriha (sanctum), the mukhamantapa (assembly hall) and the Nandi mandapa (pavilion) contain many carvings which showcase Dravidian architecture. The monument contains various paintings with coloured adhesive sections showcasing stories of the past that transports the viewer to ancient India. These designs are quite mind-blowing which attracts thousands of devotees and art lovers to the temple. The temple is popular for its big courtyards with golden apex. 
The temple trust and the caretaker of the ‘Chamarajeshwara’ temple has dedicated yeomen services in maintaining the temple and have ensured that this religious legacy is preserved and handed over from one generation to another. Having seen the wear and tear of time and after undergoing various climatic changes, ‘Chamrajeshwara Temple looked completely out of shape in the year 2017 and was on the verge of colossal deterioration but with timely intervention of the Archaeology department of Karnataka, Temple’s Charitable Trust, restoration work began in the year 2017 and was completed in the year 2018. 
The task of restoration was humongous and the responsibility was bestowed upon ‘Savani Heritage Conservation Pvt. Ltd. Being the recipient of 7 international awards and having the expertise and experience of restoring several heritage sites, the credentials were more than enough to entrust the responsibility for “Chamarajeshwara” temple. It is a matter of pride for the locals in Chamrajnagar and devotees across the country as the ‘Chamrajeshwara Temple’ that stands today is restored, revered as before and in regal state. 
