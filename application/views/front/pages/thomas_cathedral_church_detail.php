                Christianity is one the most widely celebrated religion of the world and holds a great historic significance for ancient India. The presence of historic churches all around India is a testimony to its proud legacy. Most churches have a deep historic importance and rich cultural heritage woven in it, known for its structural beauty. style and architecture.

St. Thomas Cathedral Church is one such exemplary religious structure, a magnanimously built heritage site with a glorious historical past of 300 years and a tradition that is vibrant, relevant and equally gracious even today. The idea of St. Thomas Cathedral building was conceived by Sir John Oxinden, the East India Company's first Governor of Bombay in 1668. He was an intensely religious man who foresaw the religious needs of his men and thought of building a church in Bombay. Gerald Aungier, who succeeded him in 1669, pursued Oxinden's wish.

Building Christian churches in India during the early 17th century was a great challenge for missionarios. More than style, it was a question of how identifiable churches should be in India's varied urban and rural landscapes. New churches had to be well built, the least expensive possible, beautiful, and appropriate to their liturgical use. Western techniques such as arches, vaults, foundations, full masonry walls, stone carving, timber trusses, high towers, stairs, etc. were to be almost unknown in India before 1800, except in the treaty ports and major cities.

The foundation stone of the church was first laid in 1676, although the church was only finally consecrated for divine service in 1718 as the first Anglican Church in Mumbai (then called Bombay), within the walls of the fortified British Settlement. The Cathedral then led to the creation of the Cathedral & John Connon School in 1860, in order to provide choristers to the church. Today, the Cathedral stands as a landmark in South Mumbai and is one of the oldest churches in India. The name of nearby Church gate station refers to this church. One of the gates in the Fort which the East India Company had built to protect their settlement was the entrance to the St. Thomas Church. It was called Churchgate. Since then the entire area towards the West of the Church is called "Churchgate' even today. The street leading to the Church was originally called Churchgate Street and was later renamed like many streets in Bombay, and is now known as Veer Nariman Road.

