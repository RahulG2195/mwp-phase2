<script>
    // JavaScript to apply random flip animation to cards

    // Function to add flip-animation class to the card
    function applyFlipAnimation(card) {
        card.dataset.flip = "true";
    }

    // Function to remove flip-animation class from the card
    function removeFlipAnimation(card) {
        card.dataset.flip = "false";
    }

    // Function to get a random index within a range
    function getRandomIndex(maxIndex) {
        return Math.floor(Math.random() * maxIndex);
    }

    // Function to loop through each card and apply the flip animation randomly
    function applyRandomFlipAnimations() {
        var cards = document.getElementsByClassName('card');
        var totalCards = cards.length;
        var flippedCards = new Set();

        function getRandomCard() {
            var randomIndex = getRandomIndex(totalCards);
            if (flippedCards.size === totalCards) {
                flippedCards.clear();
            }
            while (flippedCards.has(randomIndex)) {
                randomIndex = getRandomIndex(totalCards);
            }
            flippedCards.add(randomIndex);
            return randomIndex;
        }

        setInterval(function() {
            var randomCardIndex1 = getRandomCard();
            var randomCardIndex2 = getRandomCard();
            var randomCard1 = cards[randomCardIndex1];
            var randomCard2 = cards[randomCardIndex2];

            applyFlipAnimation(randomCard1);
            applyFlipAnimation(randomCard2);

            setTimeout(function() {
                removeFlipAnimation(randomCard1);
                removeFlipAnimation(randomCard2);
            }, 2000); // Flip back after 2 seconds
        }, 1000); // Flip random cards every 5 seconds
    }

    // Run the function on page load
    window.addEventListener('load', function() {
        applyRandomFlipAnimations();
    });
</script>
